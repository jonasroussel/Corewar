#include "libft.h"

void	*ft_memalloc(const size_t size)
{
	t_byte	*ptr;

	if (!(ptr = (t_byte *)malloc(size * sizeof(t_byte))))
		return (NULL);
	ft_bzero(ptr, size);
	return (ptr);
}
